package com.edgedo.common.shiro;

public class ShiroFilterWrap {
   private ShiroFilter shiroFilter;
   private int order = 0;

   public ShiroFilter getShiroFilter() {
      return this.shiroFilter;
   }

   public void setShiroFilter(ShiroFilter shiroFilter) {
      this.shiroFilter = shiroFilter;
   }

   public ShiroFilterWrap(ShiroFilter shiroFilter) {
      this.shiroFilter = shiroFilter;
   }

   public ShiroFilterWrap(ShiroFilter shiroFilter, int order) {
      this.shiroFilter = shiroFilter;
      this.order = order;
   }

   public ShiroFilterWrap() {
   }

   public int getOrder() {
      return this.order;
   }

   public void setOrder(int order) {
      this.order = order;
   }
}
